class Solution:
    def comvertToTitle(self, columnNumber: int) -> str:
        result = ""
        while columnNumber > 0:
            columnNumber -= 1  # Сдвигаем на 0‑базовую систему
            result = chr(columnNumber % 26 + ord('A')) + result
            columnNumber //= 26
        return result

# Сдвиг на 0‑базовую систему
    # Excel использует 1‑базовую нумерацию (A=1, B=2, …, Z=26).
    # Мы временно переводим в 0‑базовую (A=0, B=1, …, Z=25), вычитая 1.

# Извлечение текущей буквы
    # columnNumber % 26 даёт остаток от деления (0–25).
    # ord('A') + остаток даёт ASCII‑код буквы.
    # chr(...) преобразует код в символ.

# Переход к следующему «разряду»
    # columnNumber //= 26 делит число на 26, переходя к следующему символу слева.
    # Сборка результата
    # Новая буква добавляется в начало строки (result), так как мы идём от младших разрядов к старшим.